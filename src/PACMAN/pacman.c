int PACMAN_Validate_Move(uint8_t direction) {
	switch (direction) {
		case 0:
			if (PACMAN_Check_Path(pacmanX+2, pacmanY) && PACMAN_TelePort()) return 1;
			break;
		case 1:
			if (PACMAN_Check_Path(pacmanX-2, pacmanY) && PACMAN_TelePort()) return 1;
			break;
		case 2:
			if (PACMAN_Check_Path(pacmanX, pacmanY-2)) return 1;
			break;
		case 3:
			if (PACMAN_Check_Path(pacmanX, pacmanY+2)) return 1;
			break;
		default:
			break;
	}
	return 0;
};
